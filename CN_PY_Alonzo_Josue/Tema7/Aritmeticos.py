def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplicacion(a, b):
    return a * b

def division(a, b):
    if b != 0:
        return a / b
    else:
        return "Error: División por cero"

def modulo(a, b):
    if b != 0:
        return a % b
    else:
        return "Error: Módulo por cero"

def potencia(a, b):
    return a ** b

